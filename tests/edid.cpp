BOOST_AUTO_TEST_CASE(ChecksumTest) {
    unsigned char edid[128] = {
        0x00,0xFF,0xFF,0xFF,0xFF,0xFF,0xFF,0x00,
        0x10,0xAC,0xE6,0xD0,0x55,0x5A,0x4A,0x30,0x24,0x1D,
        0x01,0x04,
        0xA5,0x3C,0x22,0x78,0xFB,
        0x6C,0xE5,0xA5,0x55,0x50,0xA0,0x23,0x0B,0x50,0x54,
        0x00,0x02,0x00,
        0xD1,0xC0,0x01,0x01,0x01,0x01,0x01,0x01,0x01,0x01,0x01,0x01,0x01,0x01,0x01,0x01,
        0x58,0xE3,0x00,0xA0,0xA0,0xA0,0x29,0x50,0x30,0x20,0x35,0x00,0x55,0x50,0x21,0x00,0x00,0x1A,
        0x00,0x00,0x00,0xFF,0x00,0x37,0x4A,0x51,0x58,0x42,0x59,0x32,0x0A,0x20,0x20,0x20,0x20,0x20,
        0x00,0x00,0x00,0xFC,0x00,0x53,0x32,0x37,0x31,0x39,0x44,0x47,0x46,0x0A,0x20,0x20,0x20,0x20,
        0x00,0x00,0x00,0xFD,0x00,0x28,0x9B,0xFA,0xFA,0x40,0x01,0x0A,0x20,0x20,0x20,0x20,0x20,0x20,
        0x00,
        0x2C
    };

    EDID myEdid = *reinterpret_cast<EDID *>(edid);
    myEdid.commit();

    BOOST_ASSERT(myEdid.checksum == edid[127]);
}

BOOST_AUTO_TEST_CASE(EdidOutputTest) {
    EDID myEdid = Monidroid::CUSTOM_EDID;
    myEdid.setDefaultMode(1920, 1080, 120);
    myEdid.commit();

    const Monidroid::u8 *raw = reinterpret_cast<const Monidroid::u8 *>(&myEdid);

    std::ofstream f("lul.txt");

    f << std::hex << std::setfill('0') << std::uppercase;

    int i = 0;
    while (i < 128) {
        f << "0x" << std::setw(2) << static_cast<int>(raw[i++]) << ',';
    }
}
